    private void botonPeliculasCiudadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonPeliculasCiudadActionPerformed
        String reportName=PATH+INFORME_PELICULAS_CIUDAD;
        Map<String,Object> params=new HashMap<>();
        params.put("cityParameter",botonParametro.getText());
        Connection conn=ConnectionManager.getMySqlConnection();
        
       
        try {
           
            JasperPrint print=JasperFillManager.fillReport(reportName,params,conn);
            JasperViewer viewer=new JasperViewer(print);
            viewer.setVisible(true);
        } catch (JRException ex) {
            Logger.getLogger(App.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    
    }//GEN-LAST:event_botonPeliculasCiudadActionPerformed

